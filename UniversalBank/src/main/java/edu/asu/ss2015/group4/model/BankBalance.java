package edu.asu.ss2015.group4.model;

public class BankBalance {

	private double checking;
	private double saving;

	public double getCheckingAccount() {
		return checking;
	}

	public double getSavingAccount() {
		return saving;
	}

	public void setCheckingAccount(double checking) {
		this.checking = checking;
	}

	public void setSavingsAccount(double saving) {
		this.saving = saving;
	}

}
